void MPU6050_Init(void)
{
    uint8_t check;
    uint8_t Data;

    HAL_I2C_Mem_Read(&hi2c1, MPU6050_ADDR, 0x75, 1, &check, 1, HAL_MAX_DELAY);
    if (check == 0x68)
    {
        Data = 0;
        HAL_I2C_Mem_Write(&hi2c1, MPU6050_ADDR, PWR_MGMT_1, 1, &Data, 1, HAL_MAX_DELAY);
    }
}

void Read_Accel(void)
{
    uint8_t Rec_Data[6];
    HAL_I2C_Mem_Read(&hi2c1, MPU6050_ADDR, ACCEL_XOUT_H, 1, Rec_Data, 6, HAL_MAX_DELAY);

    Accel_X = (int16_t)(Rec_Data[0] << 8 | Rec_Data[1]);

    Ax = Accel_X / 16384.0f;
}

/* === Ultrasonic Code with Timeout === */



void measure_distance(void)
{
    uint32_t start_time = 0, stop_time = 0, pulse_duration = 0;

    // Send 10Âµs trigger pulse
    HAL_GPIO_WritePin(GPIOA, GPIO_PIN_7, GPIO_PIN_RESET);
    delay_us(2);
    HAL_GPIO_WritePin(GPIOA, GPIO_PIN_7, GPIO_PIN_SET);
    delay_us(10);
    HAL_GPIO_WritePin(GPIOA, GPIO_PIN_7, GPIO_PIN_RESET);

    // Wait for echo to go HIGH
    uint32_t timeout = HAL_GetTick() + 10;  // 10ms timeout
    while (HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_6) == GPIO_PIN_RESET) {
        if (HAL_GetTick() > timeout) return;  // Timeout exit
    }

    // Echo HIGH, start measuring
    __HAL_TIM_SET_COUNTER(&htim3, 0);  // reset counter
    while (HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_6) == GPIO_PIN_SET) {
        if (__HAL_TIM_GET_COUNTER(&htim3) > 30000) return;  // 30 ms timeout (~500 cm)
    }

    // Get echo time
    pulse_duration = __HAL_TIM_GET_COUNTER(&htim3);

    // Convert to distance
    distance_cm = (pulse_duration * 0.0343f) / 2.0f;
}



/* === IR Speed Measurement === */
void check_ir_speed(void)
{
    uint32_t current_time = HAL_GetTick();
    uint8_t ir_state = HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_0);

    if (ir_state == GPIO_PIN_RESET && last_ir_state == GPIO_PIN_SET)
    {
        uint32_t now = current_time;
        if (last_pulse_time != 0)
        {
            float period_s = (now - last_pulse_time) / 1000.0f;
            wheel_speed_cm_s = wheel_circumference_cm / period_s;
        }
        last_pulse_time = now;
    }

    last_ir_state = ir_state;
}
